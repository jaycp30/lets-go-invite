let lastGenerated = {};

async function generateInvite() {
  const name     = document.getElementById('recipientName').value.trim();
  const activity = document.getElementById('activity').value.trim();
  const date     = document.getElementById('date').value;
  const note     = document.getElementById('note').value.trim();
  const provider = document.querySelector('input[name="provider"]:checked').value;

  if (!name || !activity || !date) {
    alert('Please fill in name, activity, and date.');
    return;
  }

  const btn = document.getElementById('generateBtn');
  btn.disabled = true;
  btn.textContent = 'Generating…';

  try {
    const res = await fetch(`${API_URL}/generate`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ type: 'invite', name, activity, date, note, provider }),
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);

    const data = await res.json();
    lastGenerated = {
      name, activity, date,
      message:        data.message,
      mascotIntro:    data.mascotIntro,
      svgMascot:      data.svgMascot      || FALLBACK_SVG,
      buttonAnimCSS:  data.buttonAnimCSS  || FALLBACK_BUTTON_ANIM,
      confettiCSS:    data.confettiCSS    || FALLBACK_CONFETTI_CSS,
    };

    showPreview(data.message, activity, date, data.provider, lastGenerated.svgMascot);
  } catch (err) {
    console.error(err);
    alert('Something went wrong generating the invite. Please try again.');
  } finally {
    btn.disabled = false;
    btn.textContent = 'Generate Invite';
  }
}

function showPreview(message, activity, date, provider, svgMascot) {
  const preview      = document.getElementById('preview');
  const badge        = document.getElementById('providerBadge');
  const msgEl        = document.getElementById('previewMessage');
  const metaEl       = document.getElementById('previewMeta');
  const shareSection = document.getElementById('shareSection');

  badge.textContent = `Generated by ${provider === 'openai' ? 'OpenAI' : 'Claude'}`;
  msgEl.textContent = message;
  metaEl.innerHTML  = `<span>📅 ${formatDate(date)}</span><span>🎯 ${activity}</span>`;

  // Show mascot preview on generator page too
  const previewMascot = document.getElementById('previewMascot');
  if (previewMascot && svgMascot) previewMascot.innerHTML = svgMascot;

  preview.style.display      = 'block';
  shareSection.style.display = 'block';
  preview.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}

function copyShareLink() {
  const { name, activity, date, message, mascotIntro, svgMascot, buttonAnimCSS, confettiCSS } = lastGenerated;
  if (!message) return;

  const params = new URLSearchParams({ name, activity, date, message });
  if (mascotIntro)   params.set('intro',      mascotIntro);
  if (svgMascot)     params.set('svg',         svgMascot);
  if (buttonAnimCSS) params.set('btnAnim',     buttonAnimCSS);
  if (confettiCSS)   params.set('confetti',    confettiCSS);

  const url = `${window.location.origin}/invite.html?${params.toString()}`;

  navigator.clipboard.writeText(url).then(() => {
    shareBtn.textContent = '✅ Link Copied!';
    setTimeout(() => { shareBtn.textContent = '🔗 Copy Invite Link'; }, 2500);
  }).catch(() => prompt('Copy this link:', url));
}

function formatDate(raw) {
  if (!raw) return '';
  const d = new Date(raw);
  return isNaN(d) ? raw : d.toLocaleString(undefined, {
    weekday: 'short', month: 'short', day: 'numeric',
    hour: 'numeric', minute: '2-digit',
  });
}

